function squareIndex(square: Square): number {
  const file = square.charCodeAt(0) - 'a'.charCodeAt(0);
  const rank = 8 - parseInt(square[1], 10);
  return rank * 8 + file;
}

function isEndgame(chess: Chess): boolean {
  const board = chess.board();
  let queens = 0;
  let minorsAndRooks = 0;
  for (const row of board) {
    for (const cell of row) {
      if (!cell) continue;
      if (cell.type === 'q') queens++;
      if (cell.type === 'n' || cell.type === 'b' || cell.type === 'r') minorsAndRooks++;
    }
  }
  return queens === 0 || minorsAndRooks <= 2;
}

/** Static evaluation in centipawns from White's perspective (positive = White is better). */
export function evaluate(chess: Chess): number {
  if (chess.isCheckmate()) {
    // Side to move is checkmated.
    return chess.turn() === 'w' ? -100000 : 100000;
  }
  if (chess.isDraw() || chess.isStalemate() || chess.isThreefoldRepetition()) {
    return 0;
  }

  const endgame = isEndgame(chess);
  const board = chess.board();
  let score = 0;

  for (let rank = 0; rank < 8; rank++) {
    for (let file = 0; file < 8; file++) {
      const cell = board[rank][file];
      if (!cell) continue;
      const { type, color } = cell as { type: PieceSymbol; color: Color };
      const idx = rank * 8 + file;
      const pstTable = type === 'k' && endgame ? KING_ENDGAME_PST : PST[type];
      const pstIndex = color === 'w' ? idx : flipIndex(idx);
      const value = PIECE_VALUES[type] + pstTable[pstIndex];
      score += color === 'w' ? value : -value;
    }
  }

  return score;
}

function flipIndex(idx: number): number {
  const rank = Math.floor(idx / 8);
  const file = idx % 8;
  return (7 - rank) * 8 + file;
}

